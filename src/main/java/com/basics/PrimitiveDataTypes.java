package com.basics;

class PrimitiveDataTypes {
    boolean Boolean = true; // 1 bit, true or false
    byte Byte = 127; // 8 bit, -128 to 127
    char Char = 'c';
    short Short = 32767; // 16 bit, 32,768 to 32,767
    int Integer = 2147483647; // 32 bit, -2147483648 to 2147483647
    float FloatingPoint = 10/3; // 32 bit IEEE 754 floating point,
    long Long = 123456789; // 64 bit , -2^63 to (2^63)-1
    double Double = 10.0/3; // 64 bit IEEE 754 floating point,
}
